#include "libft.h"

void	ft_free_tab(void ***tab)
{
	int i;

	i = 0;
	while ((*tab)[i])
	{
		ft_memdel((void **)&(*tab)[i]);
		i++;
	}
	ft_memdel((void *)tab);
}
